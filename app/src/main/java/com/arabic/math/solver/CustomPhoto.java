package com.arabic.math.solver;

public class CustomPhoto {
    String uri_string;
    String bucket_display_name;
    String name;
    String filepath ;

    public CustomPhoto(String uri_string, String bucket_display_name, String name, String filepath) {
        this.uri_string = uri_string;
        this.bucket_display_name = bucket_display_name;
        this.name = name;
        this.filepath = filepath;
    }
}
